# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def preorderTraversal(self, root: Optional[TreeNode]) -> List[int]:
        list = []
        res = []
        while root or list :
            if root:
                res.append(root.val)
                if root.right:
                    list.append(root.right)
                root = root.left
            else:
                root  = list.pop()
        return res
