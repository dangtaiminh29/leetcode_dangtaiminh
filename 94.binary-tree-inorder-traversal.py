# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def inorderTraversal(self, root: Optional[TreeNode]) -> List[int]:
        st1 = []
        st2 = []
        while root or st1:
            while root:
                st1.append(root)
                root = root.left
            root = st1.pop()
            st2.append(root.val)
            root = root.right
        return st2
